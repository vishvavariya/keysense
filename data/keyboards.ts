export function getKeyboardById(id: string): KeyboardModel | undefined {
  return keyboards.find((kb) => kb.id === id);
}

export function getKeyboardsByCategory(category: KeyboardModel['category']): KeyboardModel[] {
  return keyboards.filter((kb) => kb.category === category);
}

export function getKeyboardsByBrand(brand: string): KeyboardModel[] {
  return keyboards.filter((kb) => kb.brand === brand);
}

